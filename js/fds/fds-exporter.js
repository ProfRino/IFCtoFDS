(function (ns) {
    function FdsExporter() {}

    FdsExporter.prototype.exportFromIfcScene = function (ifcScene, options) {
        options = options || {};
        var mode = options.mode || 'obst';
        var cellSize = Number(options.cellSize || 0.1);
        var voxelSize = Number(options.voxelSize || cellSize || 0.1);
        var openingThickening = Math.max(0, Number(options.openingThickening) || 0);
        var openingVoxelPolicy = normalizeOpeningVoxelPolicy(options.openingVoxelPolicy);
        var elements = exportableElements(ifcScene, options);
        var bounds = paddedBounds(boundsForElements(elements), Math.max(0.5, voxelSize));
        var lines = [];

        lines.push("&HEAD CHID='ifc_conversion', TITLE='Generated by IFCtoFDS' /");
        lines.push('! Conversion strategy: ' + mode);
        lines.push('! VOXEL OBST = stair-stepped OBST approximation; GEOM = detailed triangles; HYBRID = exact boxes where safe, triangles otherwise.');
        lines.push(options.includeFillSolids
            ? '! IFC window and door fill solids are exported as closed obstructions.'
            : '! IFC window and door fill solids are excluded from obstruction export.');
        if (mode !== 'geom') lines.push('! Host IFC opening volumes carve cells wherever voxel OBST geometry is emitted.');
        if (mode !== 'geom') lines.push('! Opening depth margin: ' + formatNumber(openingThickening) + ' m at each end of the penetration axis.');
        if (mode !== 'geom') lines.push('! Hole voxel policy: ' + openingVoxelPolicy + '.');
        if (options.includeFillSolids) lines.push('! Closed fill option: IFC window and door fills are exported as solids.');
        if (options.includeReviewSolids) lines.push('! Review object option: non-structural IFC review objects are exported as solids.');
        if (forcedObstStepIds(options).length) lines.push('! Manual OBST overrides: ' + forcedObstStepIds(options).join(', ') + '.');
        lines.push('! FDS mesh target cell size: ' + formatNumber(cellSize) + ' m.');
        if (mode !== 'geom') lines.push('! Voxel size: ' + formatNumber(voxelSize) + ' m. Adjacent occupied cells are merged into equivalent OBST cuboids.');
        lines.push('&TIME T_END=0.0 /');

        if (bounds) {
            lines.push(formatMesh(bounds, cellSize));
        } else {
            lines.push("&MESH ID='Domain', IJK=10,10,10, XB=0,10,0,10,0,3 /");
            lines.push('! No IFC geometry was available when this file was exported.');
        }

        lines.push("&SURF ID='IFC_SOLID', RGB=185,185,185 /");
        lines.push("&SURF ID='IFC_DOOR', RGB=150,88,46 /");
        lines.push("&SURF ID='IFC_WINDOW', RGB=92,178,214 /");

        var excludedItemIds = excludedItemIdSet(options);
        var onProgress = typeof options.onProgress === 'function' ? options.onProgress : null;
        var total = elements.length;
        // Window/door closures are deferred to the END of the namelist so that wall &HOLEs
        // (which only carve PRIOR &OBSTs) don't accidentally punch through the closure
        // that fills the same opening volume. The order FDS sees is:
        //   1. wall OBST  →  2. wall HOLE (carves wall)  →  3. window closure OBST (refills)
        // Without this deferral, a parser-order quirk could put the closure before its host
        // wall's HOLE and the HOLE would carve the window glass too.
        var deferredClosures = [];
        // Fresh allocator per export so duplicate IFC names (e.g. "Wand-001" copied across
        // floors) get :2/:3/... suffixes instead of all collapsing to the same FDS ID.
        // First occurrence keeps the bare name so test assertions that pin exact names
        // still match.
        var allocateId = makeIdAllocator();
        elements.forEach(function (element, index) {
            if (onProgress && (index & 0xF) === 0) {
                onProgress(index / Math.max(1, total), element.name || (element.ifcType + ' #' + element.stepId));
            }
            var elementMode = strategyForElement(mode, element, options);
            var elementId = allocateId(element.name, index + 1);
            if (elementMode === 'geom') {
                var geomBlock = formatGeom(element, index + 1, elementId);
                if (!isExcludedItemId(elementId, excludedItemIds)) {
                    lines.push(geomBlock);
                }
            } else if (elementMode === 'voxel') {
                formatVoxelObsts(element, index + 1, voxelSize, openingThickening, openingVoxelPolicy, excludedItemIds, elementId).forEach(function (line) {
                    lines.push(line);
                });
            } else if (elementMode === 'obst_holes') {
                formatObstWithHoles(element, index + 1, openingThickening, excludedItemIds, elementId).forEach(function (line) {
                    lines.push(line);
                });
            } else if (elementMode === 'closure') {
                if (!isExcludedItemId(elementId, excludedItemIds)) {
                    deferredClosures.push(formatClosedFillObst(element, index + 1, voxelSize, openingVoxelPolicy, elementId, !!options.useObstHoles));
                }
            } else {
                if (!isExcludedItemId(elementId, excludedItemIds)) {
                    lines.push(formatBoundingObst(element, index + 1, elementId));
                }
            }
        });
        if (deferredClosures.length) {
            lines.push('! Window/door closure OBSTs come after every wall HOLE so HOLEs do not carve the glass.');
            deferredClosures.forEach(function (line) { lines.push(line); });
        }
        if (onProgress) onProgress(1, '');

        lines.push('&TAIL /');
        return lines.join('\n');
    };

    FdsExporter.prototype.summarizeStrategy = function (ifcScene, modeOrOptions) {
        var options = typeof modeOrOptions === 'string' ? { mode: modeOrOptions } : (modeOrOptions || {});
        var mode = options.mode || 'obst';
        var voxelSize = Number(options.voxelSize || options.cellSize || 0.1);
        var openingThickening = Math.max(0, Number(options.openingThickening) || 0);
        var openingVoxelPolicy = normalizeOpeningVoxelPolicy(options.openingVoxelPolicy);
        var elements = exportableElements(ifcScene, options);
        var summary = {
            mode: mode,
            elements: elements.length,
            obsts: 0,
            voxels: 0,
            geoms: 0,
            triangles: 0
        };

        elements.forEach(function (element) {
            var elementMode = strategyForElement(summary.mode, element, options);
            if (elementMode === 'geom') {
                summary.geoms += 1;
                summary.triangles += element.mesh && element.mesh.faces ? element.mesh.faces.length : 0;
            } else if (elementMode === 'voxel') {
                var voxelized = voxelizeElement(element, voxelSize, openingThickening, openingVoxelPolicy);
                summary.obsts += voxelized.boxes.length;
                summary.voxels += voxelized.voxelCount;
            } else {
                summary.obsts += 1;
            }
        });

        return summary;
    };

    FdsExporter.prototype.getExportableElements = function (ifcScene, options) {
        return exportableElements(ifcScene, options || {});
    };

    FdsExporter.prototype.summarizeIfcMapping = function (ifcScene, options) {
        var rows = {};
        var settings = options || {};

        (ifcScene && ifcScene.elements || []).forEach(function (element) {
            var type = element.ifcType || 'IFCUNKNOWN';
            var role = roleForElement(element);
            var key = type + '|' + role;
            if (!rows[key]) {
                rows[key] = {
                    ifcType: type,
                    role: role,
                    imported: 0,
                    exported: 0
                };
            }
            rows[key].imported += 1;
            if (isExportableElement(element, settings)) rows[key].exported += 1;
        });

        return Object.keys(rows).map(function (key) {
            return rows[key];
        }).sort(function (a, b) {
            return a.ifcType.localeCompare(b.ifcType) || a.role.localeCompare(b.role);
        });
    };

    function formatMesh(bounds, cellSize) {
        var dx = Math.max(bounds.xmax - bounds.xmin, cellSize);
        var dy = Math.max(bounds.ymax - bounds.ymin, cellSize);
        var dz = Math.max(bounds.zmax - bounds.zmin, cellSize);
        var ijk = [
            Math.max(1, Math.ceil(dx / cellSize)),
            Math.max(1, Math.ceil(dy / cellSize)),
            Math.max(1, Math.ceil(dz / cellSize))
        ];
        return "&MESH ID='Domain', IJK=" + ijk.join(',') + ', XB=' + formatXb(bounds) + ' /';
    }

    function formatBoundingObst(element, index, allocatedId) {
        var bounds = inflateThinBounds(element.bounds, 0.02);
        var id = allocatedId || cleanId(element.name, index);
        return formatIfcSourceComment(element, 'OBST', id) + '\n' +
            "&OBST ID='" + escapeFdsString(id) +
            "', XB=" + formatXb(bounds) +
            ", SURF_ID='" + surfaceForElement(element) + "' /";
    }

    function formatClosedFillObst(element, index, voxelSize, openingVoxelPolicy, allocatedId, useObstHoles) {
        // With the HOLE strategy the host wall is a single non-voxelized OBST and the HOLE
        // takes the exact opening bbox — so a voxel-snapped closure leaves quantization
        // gaps. Drop the snap and use the opening's exact XZ + the wall's exact Y so the
        // closure perfectly refills the HOLE.
        var bounds = useObstHoles
            ? closedFillBoundsExact(element)
            : closedFillBounds(element, voxelSize, openingVoxelPolicy);
        var id = allocatedId || cleanId(element.name, index);
        return formatIfcSourceComment(element, 'OBST', id) + '\n' +
            "&OBST ID='" + escapeFdsString(id) +
            "', XB=" + formatXb(bounds) +
            ", SURF_ID='" + surfaceForElement(element) + "' /";
    }

    function closedFillBoundsExact(element) {
        var bounds = copyBounds(element.fillOpeningBounds || element.bounds);
        var axis = thinnestAxis(bounds);
        var hostBounds = element.fillHostBounds || bounds;
        ['x', 'y', 'z'].forEach(function (name, currentAxis) {
            var source = currentAxis === axis ? hostBounds : bounds;
            bounds[name + 'min'] = source[name + 'min'];
            bounds[name + 'max'] = source[name + 'max'];
        });
        return inflateThinBounds(bounds, 0.02);
    }

    // Returns true if every opening on the element has an axis-aligned-dominant mesh
    // (≥95% aligned face area). Required for the OBST+HOLE strategy because a HOLE is
    // axis-aligned by definition — an arched or circular opening can't be carved by HOLE.
    // True when the element's outline (projected onto the plane perpendicular to its
    // thinnest axis) fills at least `threshold` of its bounding rectangle. Used to gate the
    // OBST+HOLE strategy: an L-, U-, or C-shaped slab has bbox area much bigger than its
    // actual footprint, so emitting the bbox as a single OBST + HOLE for openings would
    // produce a rectangular slab that extends past the real outline. Those elements must
    // stay on the voxel path, which traces the outline cell-by-cell.
    function fillsBoundingBox(element, threshold) {
        var mesh = element && element.mesh;
        var b = element && element.bounds;
        if (!mesh || !mesh.vertices || !mesh.faces || !b) return false;
        var ratio = typeof threshold === 'number' ? threshold : 0.95;
        var extents = [b.xmax - b.xmin, b.ymax - b.ymin, b.zmax - b.zmin];
        // Pick the thinnest axis as the projection normal. For a flat slab → project onto
        // XY. For a thin wall → project onto XZ. Etc.
        var thinAxis = 0;
        for (var i = 1; i < 3; i += 1) {
            if (extents[i] < extents[thinAxis]) thinAxis = i;
        }
        var u = (thinAxis + 1) % 3;
        var v = (thinAxis + 2) % 3;
        var bboxArea = extents[u] * extents[v];
        if (bboxArea < 1e-6) return false;
        var unsignedArea = 0;
        mesh.faces.forEach(function (f) {
            var p0 = mesh.vertices[f[0]];
            var p1 = mesh.vertices[f[1]];
            var p2 = mesh.vertices[f[2]];
            var ex = p1[u] - p0[u], ey = p1[v] - p0[v];
            var fx = p2[u] - p0[u], fy = p2[v] - p0[v];
            unsignedArea += Math.abs(ex * fy - ey * fx) / 2;
        });
        // Flat parser mesh = single layer of triangles → unsignedArea = polygon area.
        // Solid extruded mesh = top + bottom + sides → unsignedArea ≈ 2 × polygon area (the
        // side triangles have zero projected area on the (u,v) plane).
        var divideBy = extents[thinAxis] < 1e-4 ? 1 : 2;
        var footprintArea = unsignedArea / divideBy;
        return footprintArea >= bboxArea * ratio;
    }

    function allOpeningsAxisAligned(element) {
        var openings = (element && element.openings) || [];
        if (!openings.length) return false;
        for (var i = 0; i < openings.length; i += 1) {
            var opening = openings[i];
            if (!opening || !opening.mesh) return false;
            // Wrap the opening mesh in an element-shaped object so we can reuse the existing
            // area-weighted alignment check.
            if (!isAxisAlignedDominant({ mesh: opening.mesh }, 0.95)) return false;
        }
        return true;
    }

    // Emit 1 &OBST (wall bbox) + 1 &HOLE per opening (opening bbox slightly inflated so the
    // HOLE punches all the way through the wall in the thin axis direction). Window/door
    // closure OBSTs are written later in the namelist so that wall HOLEs don't carve them.
    function formatObstWithHoles(element, index, openingThickening, excludedItemIds, allocatedId) {
        var bounds = element && element.bounds;
        if (!bounds) return [];
        var base = allocatedId || cleanId(element.name, index);
        if (isExcludedItemId(base, excludedItemIds)) return [];
        var lines = [];
        lines.push(formatIfcSourceComment(element, 'OBST', base) + '\n' +
            "&OBST ID='" + escapeFdsString(base) +
            "', XB=" + formatXb(bounds) +
            ", SURF_ID='" + surfaceForElement(element) + "' /");

        var margin = Math.max(0, Number(openingThickening) || 0);
        (element.openings || []).forEach(function (opening, oi) {
            if (!opening || !opening.mesh || !opening.mesh.vertices || !opening.mesh.vertices.length) return;
            var ob = boundsForVertices(opening.mesh.vertices);
            if (!ob) return;
            // Inflate the HOLE along the thinnest axis (the wall depth direction) so a
            // slightly-undersize opening polygon still punches all the way through and the
            // boolean cut leaves no paper-thin remnant of wall material.
            var axis = thinnestAxis(ob);
            var holeBounds = {
                xmin: ob.xmin, xmax: ob.xmax,
                ymin: ob.ymin, ymax: ob.ymax,
                zmin: ob.zmin, zmax: ob.zmax
            };
            var minKey = ['xmin', 'ymin', 'zmin'][axis];
            var maxKey = ['xmax', 'ymax', 'zmax'][axis];
            holeBounds[minKey] -= margin;
            holeBounds[maxKey] += margin;
            // Clamp the perpendicular axes to the wall bbox so the HOLE never spills into
            // neighbouring walls that happen to share a window plane.
            for (var k = 0; k < 3; k += 1) {
                if (k === axis) continue;
                var minK = ['xmin', 'ymin', 'zmin'][k];
                var maxK = ['xmax', 'ymax', 'zmax'][k];
                if (holeBounds[minK] < bounds[minK]) holeBounds[minK] = bounds[minK];
                if (holeBounds[maxK] > bounds[maxK]) holeBounds[maxK] = bounds[maxK];
            }

            var suffix = '_h' + (oi + 1);
            var holeId = base.slice(0, Math.max(1, 48 - suffix.length)) + suffix;
            lines.push("&HOLE ID='" + escapeFdsString(holeId) +
                "', XB=" + formatXb(holeBounds) + " /");
        });
        return lines;
    }

    function formatVoxelObsts(element, index, voxelSize, openingThickening, openingVoxelPolicy, excludedItemIds, allocatedId) {
        var voxelized = voxelizeElement(element, voxelSize, openingThickening, openingVoxelPolicy);
        var base = allocatedId || cleanId(element.name, index);
        var lines = [];
        if (voxelized.oversize && (!voxelized.boxes || !voxelized.boxes.length)) {
            // Voxel budget exhausted — emit a detailed GEOM instead. A bounding OBST would
            // miss stair holes / atrium cutouts on big slabs and produce wrong-shape output.
            if (!isExcludedItemId(base, excludedItemIds)) {
                lines.push('! ' + base + ': voxel budget exceeded (' + voxelized.oversize
                    + ' candidate cells) — exported as detailed GEOM instead.');
                lines.push(formatGeom(element, index, base));
            }
            return lines;
        }
        voxelized.boxes.forEach(function (bounds, boxIndex) {
            var suffix = '_v' + (boxIndex + 1);
            var id = base.slice(0, Math.max(1, 48 - suffix.length)) + suffix;
            if (isExcludedItemId(id, excludedItemIds)) return;
            lines.push(formatIfcSourceComment(element, 'OBST', id) + '\n' +
                "&OBST ID='" + escapeFdsString(id) +
                "', XB=" + formatXb(bounds) +
                ", SURF_ID='" + surfaceForElement(element) + "' /");
        });
        return lines;
    }

    function excludedItemIdSet(options) {
        if (!options || !Array.isArray(options.excludedItemIds)) return null;
        var set = {};
        options.excludedItemIds.forEach(function (id) {
            if (id) set[String(id)] = true;
        });
        return set;
    }

    function isExcludedItemId(itemId, set) {
        if (!set || !itemId) return false;
        return !!set[String(itemId)];
    }

    function formatGeom(element, index, allocatedId) {
        var verts = [];
        var faces = [];
        var id = allocatedId || cleanId(element.name, index);
        element.mesh.vertices.forEach(function (v) {
            verts.push(formatNumber(v[0]), formatNumber(v[1]), formatNumber(v[2]));
        });
        element.mesh.faces.forEach(function (face) {
            faces.push(face[0] + 1, face[1] + 1, face[2] + 1, 1);
        });

        return [
            formatIfcSourceComment(element, 'GEOM', id),
            "&GEOM ID='" + escapeFdsString(id) + "', SURF_ID='" + surfaceForElement(element) + "',",
            wrapArray('      VERTS=', verts, 9),
            wrapArray('      FACES=', faces, 16),
            ' /'
        ].join('\n');
    }

    function strategyForElement(mode, element, options) {
        if (hasForcedObstOverride(element, options)) return 'voxel';
        if (roleForElement(element) === 'opening' && options && options.includeFillSolids) return 'closure';
        if (mode === 'geom') return 'geom';
        if (mode === 'hybrid') {
            var boxThreshold = (options && typeof options.boxFriendlyThreshold === 'number')
                ? options.boxFriendlyThreshold : 0.8;
            // Slider extremes are exclusive overrides: minimum (0.5) forces voxel OBST on
            // every element regardless of type or alignment; maximum (1.0) forces detailed
            // GEOM on everything, even simple boxy walls.
            if (boxThreshold <= 0.5 + 1e-6) return 'voxel';
            if (boxThreshold >= 1.0 - 1e-6) return 'geom';
            // Zero-extent guard: a slab whose parsed mesh is *flat* (parser missed the
            // extrusion, so the mesh is the 2-D polygon triangulated in place) gets
            // auto-extruded with a default thickness inside voxelizeElement. Truly
            // degenerate elements (zero extent in 2 axes) are sent to GEOM.
            var b = element.bounds;
            if (b) {
                var zeroAxes = 0;
                if (b.xmax - b.xmin < 1e-4) zeroAxes += 1;
                if (b.ymax - b.ymin < 1e-4) zeroAxes += 1;
                if (b.zmax - b.zmin < 1e-4) zeroAxes += 1;
                if (zeroAxes >= 2) return 'geom';
            }
            if (element.openings && element.openings.length) {
                // Experimental: when the user opted in to &HOLE-carved walls/slabs, take the
                // OBST+HOLE path for any axis-aligned-dominant box-friendly element whose
                // openings are themselves axis-aligned rectangles. Otherwise fall back to
                // the voxel carve (which handles arched/circular/curved openings).
                // Tolerance 0.90 (not 0.95) because real-world wall meshes carry small
                // off-axis chunks at end caps / cutout corners from the parser's
                // triangulation. The bbox-vs-bbox HOLE math doesn't care about that —
                // it only matters that the wall is roughly box-shaped and the openings
                // are themselves axis-aligned rectangles.
                //
                // fillsBoundingBox is the critical guard: an L- or U-shaped slab passes
                // every other check above (its faces are still all axis-aligned because the
                // L outline is just a planar polygon) but its bbox is much bigger than its
                // actual footprint. Without this check the OBST+HOLE strategy emits the
                // bbox as one rectangular OBST, losing the L outline. Send those to voxel.
                if (options && options.useObstHoles
                    && isBoxFriendlyType(element)
                    && isAxisAlignedDominant(element, 0.90)
                    && fillsBoundingBox(element, 0.95)
                    && allOpeningsAxisAligned(element)) {
                    return 'obst_holes';
                }
                return isAxisAlignedDominant(element) ? 'voxel' : 'geom';
            }
            if (shouldUseObstInHybrid(element)) return 'obst';
            // For the wall/slab/beam/column family we accept a more permissive aligned-area
            // threshold because mitred corner joinery is very common. Non-box-friendly types
            // (stair stringers, railings, curved roofs) stay on the strict 0.9 path.
            if (isBoxFriendlyType(element) && isAxisAlignedDominant(element, boxThreshold)) return 'voxel';
            return 'geom';
        }
        return 'voxel';
    }

    function hasForcedObstOverride(element, options) {
        var stepId = String(element && element.stepId || '').replace(/^#/, '');
        return forcedObstStepIds(options).some(function (forcedStepId) {
            return String(forcedStepId).replace(/^#/, '') === stepId;
        });
    }

    function forcedObstStepIds(options) {
        return options && Array.isArray(options.forceObstStepIds) ? options.forceObstStepIds : [];
    }

    function exportableElements(ifcScene, options) {
        return ifcScene && ifcScene.elements
            ? ifcScene.elements.filter(function (element) {
                return isExportableElement(element, options || {});
            })
            : [];
    }

    function isExportableElement(element, options) {
        if (isExcludedStepId(element, options)) return false;
        var role = roleForElement(element);
        if (role === 'opening') return !!options.includeFillSolids;
        if (role === 'review') return !!options.includeReviewSolids;
        if (role === 'ignore') return false;
        return element.convertible !== false && element.exportAsSolid !== false;
    }

    function isExcludedStepId(element, options) {
        if (!element || !options || !Array.isArray(options.excludedStepIds) || !options.excludedStepIds.length) return false;
        var stepId = String(element.stepId || '').replace(/^#/, '');
        return options.excludedStepIds.some(function (excluded) {
            return String(excluded).replace(/^#/, '') === stepId;
        });
    }

    function roleForElement(element) {
        if (element.fdsRole) return element.fdsRole;
        if (element.exportAsSolid === false) return element.convertible === false ? 'ignore' : 'opening';
        return 'solid';
    }

    var BOX_FRIENDLY_TYPES = {
        IFCWALL: true,
        IFCWALLSTANDARDCASE: true,
        IFCSLAB: true,
        // IFCROOF is included so flat roof decks (tapered-insulation roofs, concrete-deck
        // roofs) get the L/U-outline voxel treatment. Sloped roofs (gable / hip / barrel)
        // still fall through to GEOM because their large sloped faces drop the area-weighted
        // aligned ratio below the 0.8 box-friendly threshold.
        IFCROOF: true,
        IFCBEAM: true,
        IFCCOLUMN: true,
        IFCPLATE: true,
        // IFCMEMBER stays off this list: it is the type used for stair stringers (and
        // diagonal braces, sloped struts), which can still hit 85–92% aligned area
        // even though their main faces are sloped. Letting them through the permissive
        // 0.8 box-friendly path turns stair geometry into ugly block-art.
        IFCDOOR: true,
        IFCWINDOW: true,
        IFCFURNISHINGELEMENT: true,
        IFCBUILDINGELEMENTPROXY: true
    };

    function isBoxFriendlyType(element) {
        return !!BOX_FRIENDLY_TYPES[element && element.ifcType];
    }

    function shouldUseObstInHybrid(element) {
        if (!isBoxFriendlyType(element)) return false;
        // A flat polygon (parser failed to extrude the slab) is degenerate for the
        // axis-aligned-box check below: every triangle's normal points purely ±flat-axis
        // and every vertex sits at bounds.{flat}min === bounds.{flat}max, so the test
        // reports a "perfect axis-aligned box" and routes the slab to a single bounding
        // OBST — ignoring the L/U outline. Send these to voxel (the inflater rebuilds the
        // outline) instead.
        if (flatAxisFromBounds(element && element.bounds) >= 0) return false;
        // L / U / C / T shaped slabs and walls would also get collapsed to their bbox
        // here. fillsBoundingBox rejects anything whose footprint covers less than 95% of
        // its bbox — those go on to voxel so the outline is preserved cell-by-cell.
        if (!fillsBoundingBox(element, 0.95)) return false;
        return isAxisAlignedBoxLike(element);
    }

    function isAxisAlignedBoxLike(element) {
        if (!element || !element.mesh || !element.mesh.vertices || !element.mesh.faces || !element.bounds) return false;
        var b = element.bounds;
        var eps = 1e-3;
        var vertices = element.mesh.vertices;
        var faces = element.mesh.faces;

        for (var i = 0; i < faces.length; i++) {
            var f = faces[i];
            var v0 = vertices[f[0]], v1 = vertices[f[1]], v2 = vertices[f[2]];
            var ex = v1[0] - v0[0], ey = v1[1] - v0[1], ez = v1[2] - v0[2];
            var fx = v2[0] - v0[0], fy = v2[1] - v0[1], fz = v2[2] - v0[2];
            var nx = ey * fz - ez * fy;
            var ny = ez * fx - ex * fz;
            var nz = ex * fy - ey * fx;
            var nl = Math.sqrt(nx * nx + ny * ny + nz * nz);
            if (nl < 1e-10) continue;
            nx /= nl; ny /= nl; nz /= nl;

            var ax = Math.abs(nx), ay = Math.abs(ny), az = Math.abs(nz);
            var mx = Math.max(ax, ay, az);
            if (mx < 0.999) return false;

            var axis;
            if (ax >= ay && ax >= az) axis = 0;
            else if (ay >= ax && ay >= az) axis = 1;
            else axis = 2;

            var axisKey = ['x', 'y', 'z'][axis];
            var coord = v0[axis];
            var atMin = Math.abs(coord - b[axisKey + 'min']) <= eps;
            var atMax = Math.abs(coord - b[axisKey + 'max']) <= eps;
            if (!atMin && !atMax) return false;
        }
        return true;
    }

    // Area-weighted check: is the element dominantly made of axis-aligned faces?
    // Robust to opening cutouts (window/door reveals), so walls and flat slabs with
    // openings still qualify for voxelization, while sloped/curved surfaces (roofs)
    // fall through to detailed GEOM instead of being stair-stepped.
    function isAxisAlignedDominant(element, threshold) {
        if (!element || !element.mesh || !element.mesh.vertices || !element.mesh.faces) return false;
        var vertices = element.mesh.vertices;
        var faces = element.mesh.faces;
        var alignedArea = 0;
        var totalArea = 0;
        var minRatio = typeof threshold === 'number' ? threshold : 0.9;

        for (var i = 0; i < faces.length; i++) {
            var f = faces[i];
            var v0 = vertices[f[0]], v1 = vertices[f[1]], v2 = vertices[f[2]];
            var ex = v1[0] - v0[0], ey = v1[1] - v0[1], ez = v1[2] - v0[2];
            var fx = v2[0] - v0[0], fy = v2[1] - v0[1], fz = v2[2] - v0[2];
            var nx = ey * fz - ez * fy;
            var ny = ez * fx - ex * fz;
            var nz = ex * fy - ey * fx;
            var doubleArea = Math.sqrt(nx * nx + ny * ny + nz * nz);
            if (doubleArea < 1e-12) continue;

            totalArea += doubleArea;
            var mx = Math.max(Math.abs(nx), Math.abs(ny), Math.abs(nz)) / doubleArea;
            if (mx >= 0.999) alignedArea += doubleArea;
        }

        if (totalArea <= 0) return false;
        return (alignedArea / totalArea) >= minRatio;
    }

    // Returns 0/1/2 for the flat axis (x/y/z) if the mesh bounds are degenerate on exactly
    // one axis, or -1 if the bounds are 3-D (no inflation needed) or worse (two/three axes
    // collapsed — too degenerate to recover; the strategy router sends those to GEOM).
    function flatAxisFromBounds(bounds) {
        if (!bounds) return -1;
        var extents = [
            bounds.xmax - bounds.xmin,
            bounds.ymax - bounds.ymin,
            bounds.zmax - bounds.zmin
        ];
        var flat = -1;
        var flatCount = 0;
        for (var i = 0; i < 3; i += 1) {
            if (extents[i] < 1e-4) { flatCount += 1; flat = i; }
        }
        return flatCount === 1 ? flat : -1;
    }

    // Returns 0/1/2 for the axis where the bounds extent is dramatically smaller than the
    // largest (< 5% of max) — i.e. the element is a thin prism along that axis. Used to
    // enable single-slice voxelization (compute one cross-section, replicate through the
    // thin direction) which keeps huge floor-plan slabs/roofs under the candidate cap.
    function thinPrismAxis(bounds) {
        if (!bounds) return -1;
        var extents = [
            bounds.xmax - bounds.xmin,
            bounds.ymax - bounds.ymin,
            bounds.zmax - bounds.zmin
        ];
        var maxE = Math.max(extents[0], extents[1], extents[2]);
        if (maxE <= 0) return -1;
        var thinAxis = -1;
        var thinExt = Infinity;
        for (var i = 0; i < 3; i += 1) {
            if (extents[i] > 0 && extents[i] < maxE * 0.05 && extents[i] < thinExt) {
                thinAxis = i;
                thinExt = extents[i];
            }
        }
        return thinAxis;
    }

    function defaultSlabThickness(element) {
        var type = element && element.ifcType ? String(element.ifcType).toUpperCase() : '';
        if (type === 'IFCROOF') return 0.30;
        if (type === 'IFCSLAB') return 0.25;
        if (type === 'IFCWALL' || type === 'IFCWALLSTANDARDCASE') return 0.20;
        if (type === 'IFCCOVERING' || type === 'IFCPLATE') return 0.05;
        return 0.20;
    }

    // Turns a flat polygon (all vertices coplanar in one axis) into a thin closed solid by
    // duplicating the polygon along the flat axis, mirroring its triangles for the bottom
    // (with reversed winding so normals point outward), and stitching boundary edges into
    // side quads. The resulting mesh has true volume so pointInsideMesh + triangle-vs-box
    // intersections trace the polygon outline correctly during voxelization.
    function inflateFlatMesh(mesh, bounds, flatAxis, thickness) {
        if (!mesh || !mesh.vertices || !mesh.faces) return null;
        var t = Math.max(0.02, Number(thickness) || 0.2);
        var n = mesh.vertices.length;
        if (n === 0 || mesh.faces.length === 0) return null;

        var verts = new Array(n * 2);
        for (var i = 0; i < n; i += 1) {
            var v = mesh.vertices[i];
            verts[i] = [v[0], v[1], v[2]];
            var bot = [v[0], v[1], v[2]];
            bot[flatAxis] -= t;
            verts[i + n] = bot;
        }

        // Top: original winding. Bottom: shifted by n and winding reversed so the bottom
        // faces' normals point in -flatAxis (outward, since the top normals already point
        // +flatAxis after the parser triangulated the polygon).
        var topFaces = mesh.faces.map(function (f) { return [f[0], f[1], f[2]]; });
        var bottomFaces = mesh.faces.map(function (f) { return [f[0] + n, f[2] + n, f[1] + n]; });

        // Boundary edges = edges that appear in exactly one face (== outline of the slab).
        var edgeCounts = {};
        function edgeKey(a, b) { return a < b ? a + '_' + b : b + '_' + a; }
        mesh.faces.forEach(function (f) {
            var edges = [[f[0], f[1]], [f[1], f[2]], [f[2], f[0]]];
            edges.forEach(function (e) {
                var k = edgeKey(e[0], e[1]);
                edgeCounts[k] = (edgeCounts[k] || 0) + 1;
            });
        });
        var sideFaces = [];
        mesh.faces.forEach(function (f) {
            var edges = [[f[0], f[1]], [f[1], f[2]], [f[2], f[0]]];
            edges.forEach(function (e) {
                if (edgeCounts[edgeKey(e[0], e[1])] !== 1) return;
                var a = e[0], b = e[1];
                // Two triangles per boundary edge connecting top edge (a, b) to its bottom
                // twin (a + n, b + n). Winding chosen so the side normal points outward —
                // matches the top face that owns this edge.
                sideFaces.push([a, b, b + n]);
                sideFaces.push([a, b + n, a + n]);
            });
        });

        var inflatedBounds = {
            xmin: bounds.xmin, xmax: bounds.xmax,
            ymin: bounds.ymin, ymax: bounds.ymax,
            zmin: bounds.zmin, zmax: bounds.zmax
        };
        var minKey = ['xmin', 'ymin', 'zmin'][flatAxis];
        inflatedBounds[minKey] -= t;

        return {
            mesh: { vertices: verts, faces: topFaces.concat(bottomFaces, sideFaces) },
            bounds: inflatedBounds
        };
    }

    function voxelizeElement(element, voxelSize, openingThickening, openingVoxelPolicy) {
        var size = Math.max(0.01, Number(voxelSize) || 0.1);
        var holePolicy = normalizeOpeningVoxelPolicy(openingVoxelPolicy);
        var mesh = element && element.mesh;
        var bounds = element && element.bounds;
        if (!mesh || !mesh.vertices || !mesh.faces || !bounds) return { boxes: [], voxelCount: 0 };

        // Some IFC files (notably the school file's Basic Roof / Floor slabs) deliver slab
        // representations that the parser couldn't extrude, so element.mesh ends up as the
        // 2-D floor-plan polygon triangulated in place — every vertex at the same Z, no
        // enclosed volume for point-in-mesh tests. Auto-inflate that polygon downward into
        // a thin solid so the existing voxelizer can trace the actual L / U outline.
        var workingMesh = mesh;
        var workingBounds = bounds;
        var flatAxis = flatAxisFromBounds(bounds);
        if (flatAxis >= 0) {
            var inflated = inflateFlatMesh(mesh, bounds, flatAxis, defaultSlabThickness(element));
            if (inflated) {
                workingMesh = inflated.mesh;
                workingBounds = inflated.bounds;
            }
        }

        // Beyond the perfectly-flat case, a *thin* axis-aligned prism (concrete-deck roof
        // ~0.4 m thick over a 60 m × 50 m footprint, or a slab with real but small Z extent)
        // also collapses to a one-slice rasterization. Require both a clear thin axis AND
        // ≥95% axis-aligned faces — without the alignment check we'd wrongly prismatize a
        // thin sloped roof and stair-step its slope. Skip for elements with openings to
        // avoid carving an opening through every Z layer when the opening is local to one.
        var prismAxis = flatAxis;
        if (prismAxis < 0) {
            var maybeThin = thinPrismAxis(workingBounds);
            if (maybeThin >= 0
                && (!element.openings || !element.openings.length)
                && isAxisAlignedDominant(element, 0.95)) {
                prismAxis = maybeThin;
            }
        }

        var epsilon = size * 1e-7;
        var range = {
            xmin: Math.floor(workingBounds.xmin / size),
            xmax: Math.ceil((workingBounds.xmax - epsilon) / size),
            ymin: Math.floor(workingBounds.ymin / size),
            ymax: Math.ceil((workingBounds.ymax - epsilon) / size),
            zmin: Math.floor(workingBounds.zmin / size),
            zmax: Math.ceil((workingBounds.zmax - epsilon) / size)
        };
        // For a flat-inflated slab (parser failed to extrude), occupancy is identical on
        // every Z layer — the inflated solid is a prism. Compute one layer and replicate it
        // up so the L/U outline is preserved without paying for k× the per-cell work and
        // without blowing the candidate cap on a 60 m × 50 m floor plan.
        var effectiveCount = (range.xmax - range.xmin) * (range.ymax - range.ymin) * (range.zmax - range.zmin);
        var prismMode = prismAxis >= 0;
        if (prismMode) {
            // The slice loop below collapses the prism axis to a single layer, so multiply
            // out two dimensions instead of three.
            var dimX = range.xmax - range.xmin;
            var dimY = range.ymax - range.ymin;
            var dimZ = range.zmax - range.zmin;
            if (prismAxis === 0) effectiveCount = 1 * dimY * dimZ;
            else if (prismAxis === 1) effectiveCount = dimX * 1 * dimZ;
            else effectiveCount = dimX * dimY * 1;
        }
        // Hard ceiling on the *effective* candidate count: above this, voxelization runs
        // into minutes per element on huge foundation pads. Surface the cap upstream so the
        // caller can fall back to GEOM (which keeps opening cutouts) instead of a misleading
        // rectangular bounding OBST. Prism mode brings huge flat slabs comfortably under
        // the cap because the work scales with one slice instead of all k layers.
        if (effectiveCount > 500000) {
            return { boxes: [], voxelCount: 0, oversize: effectiveCount };
        }

        var triangles = prepareTriangles(workingMesh);
        var openingTriangleSets = [];
        // Parallel array of bboxes (with the same axis-thickening as the triangle sets).
        // For axis-aligned-dominant openings (the common window/door case) the bbox is the
        // opening, and the cheap bbox test is robust against the ray-casting boundary bugs
        // that left "phantom holes" one cell wide along the opening edges.
        var openingBboxes = [];
        (element.openings || []).forEach(function (opening) {
            var sets = prepareOpeningTriangleSets(opening.mesh, openingThickening);
            var bbox = inflatedOpeningBbox(opening.mesh, openingThickening);
            var axisAligned = !!opening.mesh && isAxisAlignedDominant({ mesh: opening.mesh }, 0.95);
            sets.forEach(function (trianglesForOpening) {
                if (!trianglesForOpening.length) return;
                openingTriangleSets.push(trianglesForOpening);
                openingBboxes.push(axisAligned && bbox ? bbox : null);
            });
        });
        var occupied = {};
        var halfSize = Math.max(size / 2 - epsilon, size * 0.499);

        if (prismMode) {
            // Compute occupancy on one slice mid-way through the prism axis, then duplicate
            // it into every layer along that axis. For a true axis-aligned prism (flat slab
            // after inflation, or a thin axis-aligned roof) the cross-section is invariant
            // along the prism axis, so one slice = the whole element.
            var sliceIdxX = range.xmin + Math.floor((range.xmax - range.xmin) / 2);
            var sliceIdxY = range.ymin + Math.floor((range.ymax - range.ymin) / 2);
            var sliceIdxZ = range.zmin + Math.floor((range.zmax - range.zmin) / 2);
            var occupiedSlice = {};
            for (var siy = range.ymin; siy < range.ymax; siy += 1) {
                if (prismAxis === 1 && siy !== sliceIdxY) continue;
                for (var six = range.xmin; six < range.xmax; six += 1) {
                    if (prismAxis === 0 && six !== sliceIdxX) continue;
                    for (var siz = range.zmin; siz < range.zmax; siz += 1) {
                        if (prismAxis === 2 && siz !== sliceIdxZ) continue;
                        var sc = [
                            (six + 0.5) * size,
                            (siy + 0.5) * size,
                            (siz + 0.5) * size
                        ];
                        if (voxelIsOccupied(triangles, sc, halfSize) &&
                            !openingCarvesVoxel(openingTriangleSets, openingBboxes, sc, halfSize, holePolicy)) {
                            occupiedSlice[voxelKey(six, siy, siz)] = true;
                        }
                    }
                }
            }
            // Replicate the slice into every layer along the prism axis.
            Object.keys(occupiedSlice).forEach(function (key) {
                var parts = key.split(',');
                var bx = parseInt(parts[0], 10);
                var by = parseInt(parts[1], 10);
                var bz = parseInt(parts[2], 10);
                if (prismAxis === 0) {
                    for (var rx = range.xmin; rx < range.xmax; rx += 1) {
                        occupied[voxelKey(rx, by, bz)] = true;
                    }
                } else if (prismAxis === 1) {
                    for (var ry = range.ymin; ry < range.ymax; ry += 1) {
                        occupied[voxelKey(bx, ry, bz)] = true;
                    }
                } else {
                    for (var rz = range.zmin; rz < range.zmax; rz += 1) {
                        occupied[voxelKey(bx, by, rz)] = true;
                    }
                }
            });
        } else {
            for (var iz = range.zmin; iz < range.zmax; iz += 1) {
                for (var iy = range.ymin; iy < range.ymax; iy += 1) {
                    for (var ix = range.xmin; ix < range.xmax; ix += 1) {
                        var center = [
                            (ix + 0.5) * size,
                            (iy + 0.5) * size,
                            (iz + 0.5) * size
                        ];
                        if (voxelIsOccupied(triangles, center, halfSize) &&
                            !openingCarvesVoxel(openingTriangleSets, openingBboxes, center, halfSize, holePolicy)) {
                            occupied[voxelKey(ix, iy, iz)] = true;
                        }
                    }
                }
            }
        }

        var voxelCount = Object.keys(occupied).length;
        return {
            boxes: mergeOccupiedVoxels(occupied, size),
            voxelCount: voxelCount
        };
    }

    function prepareOpeningTriangleSets(mesh, openingThickening) {
        var margin = Math.max(0, Number(openingThickening) || 0);
        if (!mesh || !mesh.vertices || !mesh.faces) return [];
        if (!margin) return splitTrianglesByConnectedFaces(mesh);

        var bounds = boundsForVertices(mesh.vertices);
        var axis = thinnestAxis(bounds);
        var minKey = ['xmin', 'ymin', 'zmin'][axis];
        var maxKey = ['xmax', 'ymax', 'zmax'][axis];
        var center = (bounds[minKey] + bounds[maxKey]) / 2;
        var inflated = {
            vertices: mesh.vertices.map(function (vertex) {
                var copy = vertex.slice();
                if (copy[axis] < center) copy[axis] -= margin;
                if (copy[axis] > center) copy[axis] += margin;
                return copy;
            }),
            faces: mesh.faces
        };
        return splitTrianglesByConnectedFaces(inflated);
    }

    // Same axis-thickening as prepareOpeningTriangleSets, but returns the bbox of the
    // inflated polygon rather than its triangles. Used by openingCarvesVoxel as a robust
    // fast path: for axis-aligned-rectangular openings (almost every IFC window/door) the
    // bbox IS the opening volume, and a simple point-in-bbox test is exact and immune to
    // the ray-casting parity bug that pointInsideMesh suffers at axis-aligned mesh corners.
    function inflatedOpeningBbox(mesh, openingThickening) {
        if (!mesh || !mesh.vertices || !mesh.vertices.length) return null;
        var bounds = boundsForVertices(mesh.vertices);
        if (!bounds) return null;
        var margin = Math.max(0, Number(openingThickening) || 0);
        if (!margin) return bounds;
        var axis = thinnestAxis(bounds);
        var minKey = ['xmin', 'ymin', 'zmin'][axis];
        var maxKey = ['xmax', 'ymax', 'zmax'][axis];
        var inflated = {
            xmin: bounds.xmin, xmax: bounds.xmax,
            ymin: bounds.ymin, ymax: bounds.ymax,
            zmin: bounds.zmin, zmax: bounds.zmax
        };
        inflated[minKey] -= margin;
        inflated[maxKey] += margin;
        return inflated;
    }

    function openingCarvesVoxel(openingTriangleSets, openingBboxes, center, halfSize, openingVoxelPolicy) {
        for (var i = 0; i < openingTriangleSets.length; i += 1) {
            var bbox = openingBboxes && openingBboxes[i];
            if (bbox) {
                // Fast and numerically exact path for axis-aligned-rectangular openings.
                // 'smaller': cell center strictly inside opening bbox → cell carved.
                // 'larger':  cell volume overlaps opening bbox (any touch) → cell carved.
                if (openingVoxelPolicy === 'smaller') {
                    if (center[0] > bbox.xmin && center[0] < bbox.xmax
                        && center[1] > bbox.ymin && center[1] < bbox.ymax
                        && center[2] > bbox.zmin && center[2] < bbox.zmax) return true;
                } else {
                    if (center[0] + halfSize > bbox.xmin && center[0] - halfSize < bbox.xmax
                        && center[1] + halfSize > bbox.ymin && center[1] - halfSize < bbox.ymax
                        && center[2] + halfSize > bbox.zmin && center[2] - halfSize < bbox.zmax) return true;
                }
                continue;
            }
            // Non-axis-aligned opening (arched window, circular porthole, etc.) — fall back
            // to the mesh-based test.
            var trianglesForOpening = openingTriangleSets[i];
            if (openingVoxelPolicy === 'smaller'
                ? pointInsideMesh(center, trianglesForOpening)
                : voxelIsOccupied(trianglesForOpening, center, halfSize)) return true;
        }
        return false;
    }

    function splitTrianglesByConnectedFaces(mesh) {
        var triangles = prepareTriangles(mesh);
        var facesByVertex = {};
        var visited = {};
        var sets = [];

        mesh.faces.forEach(function (face, faceIndex) {
            face.forEach(function (vertexIndex) {
                facesByVertex[vertexIndex] = facesByVertex[vertexIndex] || [];
                facesByVertex[vertexIndex].push(faceIndex);
            });
        });

        mesh.faces.forEach(function (face, startIndex) {
            if (visited[startIndex]) return;

            var pending = [startIndex];
            var set = [];
            visited[startIndex] = true;

            while (pending.length) {
                var faceIndex = pending.pop();
                set.push(triangles[faceIndex]);
                mesh.faces[faceIndex].forEach(function (vertexIndex) {
                    facesByVertex[vertexIndex].forEach(function (connectedFaceIndex) {
                        if (visited[connectedFaceIndex]) return;
                        visited[connectedFaceIndex] = true;
                        pending.push(connectedFaceIndex);
                    });
                });
            }

            sets.push(set);
        });

        return sets;
    }

    function prepareTriangles(mesh) {
        if (!mesh || !mesh.vertices || !mesh.faces) return [];
        return mesh.faces.map(function (face) {
            var vertices = [mesh.vertices[face[0]], mesh.vertices[face[1]], mesh.vertices[face[2]]];
            return {
                vertices: vertices,
                bounds: triangleBounds(vertices)
            };
        });
    }

    function triangleBounds(vertices) {
        return {
            xmin: Math.min(vertices[0][0], vertices[1][0], vertices[2][0]),
            xmax: Math.max(vertices[0][0], vertices[1][0], vertices[2][0]),
            ymin: Math.min(vertices[0][1], vertices[1][1], vertices[2][1]),
            ymax: Math.max(vertices[0][1], vertices[1][1], vertices[2][1]),
            zmin: Math.min(vertices[0][2], vertices[1][2], vertices[2][2]),
            zmax: Math.max(vertices[0][2], vertices[1][2], vertices[2][2])
        };
    }

    function voxelIsOccupied(triangles, center, halfSize) {
        var cellBounds = {
            xmin: center[0] - halfSize,
            xmax: center[0] + halfSize,
            ymin: center[1] - halfSize,
            ymax: center[1] + halfSize,
            zmin: center[2] - halfSize,
            zmax: center[2] + halfSize
        };

        for (var i = 0; i < triangles.length; i += 1) {
            var triangle = triangles[i];
            if (boundsOverlap(triangle.bounds, cellBounds) &&
                triangleIntersectsBox(triangle.vertices, center, halfSize)) {
                return true;
            }
        }

        return pointInsideMesh(center, triangles);
    }

    function boundsOverlap(a, b) {
        return a.xmax >= b.xmin && a.xmin <= b.xmax &&
            a.ymax >= b.ymin && a.ymin <= b.ymax &&
            a.zmax >= b.zmin && a.zmin <= b.zmax;
    }

    function triangleIntersectsBox(vertices, center, halfSize) {
        var translated = vertices.map(function (vertex) {
            return subtractVector(vertex, center);
        });
        var edges = [
            subtractVector(translated[1], translated[0]),
            subtractVector(translated[2], translated[1]),
            subtractVector(translated[0], translated[2])
        ];
        var axes = [
            [1, 0, 0],
            [0, 1, 0],
            [0, 0, 1],
            cross(edges[0], edges[1])
        ];
        var boxAxes = [[1, 0, 0], [0, 1, 0], [0, 0, 1]];

        edges.forEach(function (edge) {
            boxAxes.forEach(function (axis) {
                axes.push(cross(edge, axis));
            });
        });

        return axes.every(function (axis) {
            if (dot(axis, axis) < 1e-18) return true;
            var projections = translated.map(function (vertex) { return dot(vertex, axis); });
            var radius = halfSize * (Math.abs(axis[0]) + Math.abs(axis[1]) + Math.abs(axis[2]));
            return Math.max.apply(Math, projections) >= -radius &&
                Math.min.apply(Math, projections) <= radius;
        });
    }

    function pointInsideMesh(point, triangles) {
        var direction = [0.912870929, 0.365148372, 0.182574186];
        var hits = {};

        triangles.forEach(function (triangle) {
            var distance = rayTriangleDistance(point, direction, triangle.vertices);
            if (distance !== null && distance > 1e-8) hits[distance.toFixed(8)] = true;
        });

        return Object.keys(hits).length % 2 === 1;
    }

    function rayTriangleDistance(origin, direction, vertices) {
        var edge1 = subtractVector(vertices[1], vertices[0]);
        var edge2 = subtractVector(vertices[2], vertices[0]);
        var p = cross(direction, edge2);
        var determinant = dot(edge1, p);
        if (Math.abs(determinant) < 1e-12) return null;

        var inverse = 1 / determinant;
        var t = subtractVector(origin, vertices[0]);
        var u = dot(t, p) * inverse;
        if (u < -1e-10 || u > 1 + 1e-10) return null;

        var q = cross(t, edge1);
        var v = dot(direction, q) * inverse;
        if (v < -1e-10 || u + v > 1 + 1e-10) return null;

        var distance = dot(edge2, q) * inverse;
        return distance >= 0 ? distance : null;
    }

    function mergeOccupiedVoxels(occupied, size) {
        var visited = {};
        var boxes = [];
        var cells = Object.keys(occupied).map(function (key) {
            return key.split(',').map(Number);
        });

        cells.sort(function (a, b) {
            return a[2] - b[2] || a[1] - b[1] || a[0] - b[0];
        });

        cells.forEach(function (cell) {
            var ix = cell[0];
            var iy = cell[1];
            var iz = cell[2];
            if (!voxelAvailable(occupied, visited, ix, iy, iz)) return;

            var xmax = ix + 1;
            while (voxelAvailable(occupied, visited, xmax, iy, iz)) xmax += 1;

            var ymax = iy + 1;
            while (voxelRectangleAvailable(occupied, visited, ix, xmax, ymax, ymax + 1, iz, iz + 1)) ymax += 1;

            var zmax = iz + 1;
            while (voxelRectangleAvailable(occupied, visited, ix, xmax, iy, ymax, zmax, zmax + 1)) zmax += 1;

            markVoxelRectangle(visited, ix, xmax, iy, ymax, iz, zmax);
            boxes.push({
                xmin: ix * size,
                xmax: xmax * size,
                ymin: iy * size,
                ymax: ymax * size,
                zmin: iz * size,
                zmax: zmax * size
            });
        });

        return boxes;
    }

    function voxelAvailable(occupied, visited, ix, iy, iz) {
        var key = voxelKey(ix, iy, iz);
        return !!occupied[key] && !visited[key];
    }

    function voxelRectangleAvailable(occupied, visited, xmin, xmax, ymin, ymax, zmin, zmax) {
        for (var iz = zmin; iz < zmax; iz += 1) {
            for (var iy = ymin; iy < ymax; iy += 1) {
                for (var ix = xmin; ix < xmax; ix += 1) {
                    if (!voxelAvailable(occupied, visited, ix, iy, iz)) return false;
                }
            }
        }
        return true;
    }

    function markVoxelRectangle(visited, xmin, xmax, ymin, ymax, zmin, zmax) {
        for (var iz = zmin; iz < zmax; iz += 1) {
            for (var iy = ymin; iy < ymax; iy += 1) {
                for (var ix = xmin; ix < xmax; ix += 1) {
                    visited[voxelKey(ix, iy, iz)] = true;
                }
            }
        }
    }

    function voxelKey(ix, iy, iz) {
        return ix + ',' + iy + ',' + iz;
    }

    function subtractVector(a, b) {
        return [a[0] - b[0], a[1] - b[1], a[2] - b[2]];
    }

    function cross(a, b) {
        return [
            a[1] * b[2] - a[2] * b[1],
            a[2] * b[0] - a[0] * b[2],
            a[0] * b[1] - a[1] * b[0]
        ];
    }

    function dot(a, b) {
        return a[0] * b[0] + a[1] * b[1] + a[2] * b[2];
    }

    function pointKey(point) {
        return point.map(function (value) { return Number(value).toFixed(4); }).join(',');
    }

    function wrapArray(prefix, values, perLine) {
        var lines = [];
        for (var i = 0; i < values.length; i += perLine) {
            lines.push((i === 0 ? prefix : spaces(prefix.length)) + values.slice(i, i + perLine).join(','));
        }
        return lines.join(',\n');
    }

    function paddedBounds(bounds, margin) {
        if (!bounds) return null;
        return {
            xmin: bounds.xmin - margin,
            xmax: bounds.xmax + margin,
            ymin: bounds.ymin - margin,
            ymax: bounds.ymax + margin,
            zmin: Math.min(0, bounds.zmin - margin),
            zmax: bounds.zmax + margin
        };
    }

    function boundsForElements(elements) {
        var bounds = null;
        elements.forEach(function (element) {
            var b = element.bounds;
            if (!b) return;
            if (!bounds) {
                bounds = { xmin: b.xmin, xmax: b.xmax, ymin: b.ymin, ymax: b.ymax, zmin: b.zmin, zmax: b.zmax };
            } else {
                bounds.xmin = Math.min(bounds.xmin, b.xmin);
                bounds.xmax = Math.max(bounds.xmax, b.xmax);
                bounds.ymin = Math.min(bounds.ymin, b.ymin);
                bounds.ymax = Math.max(bounds.ymax, b.ymax);
                bounds.zmin = Math.min(bounds.zmin, b.zmin);
                bounds.zmax = Math.max(bounds.zmax, b.zmax);
            }
        });
        return bounds;
    }

    function boundsForVertices(vertices) {
        var bounds = null;
        vertices.forEach(function (vertex) {
            if (!bounds) {
                bounds = {
                    xmin: vertex[0], xmax: vertex[0],
                    ymin: vertex[1], ymax: vertex[1],
                    zmin: vertex[2], zmax: vertex[2]
                };
                return;
            }
            bounds.xmin = Math.min(bounds.xmin, vertex[0]);
            bounds.xmax = Math.max(bounds.xmax, vertex[0]);
            bounds.ymin = Math.min(bounds.ymin, vertex[1]);
            bounds.ymax = Math.max(bounds.ymax, vertex[1]);
            bounds.zmin = Math.min(bounds.zmin, vertex[2]);
            bounds.zmax = Math.max(bounds.zmax, vertex[2]);
        });
        return bounds;
    }

    function thinnestAxis(bounds) {
        var spans = [
            bounds.xmax - bounds.xmin,
            bounds.ymax - bounds.ymin,
            bounds.zmax - bounds.zmin
        ];
        var axis = 0;
        if (spans[1] < spans[axis]) axis = 1;
        if (spans[2] < spans[axis]) axis = 2;
        return axis;
    }

    function closedFillBounds(element, voxelSize, openingVoxelPolicy) {
        var bounds = copyBounds(element.fillOpeningBounds || element.bounds);
        var axis = thinnestAxis(bounds);
        var hostBounds = element.fillHostBounds || bounds;
        var size = Math.max(0.01, Number(voxelSize) || 0.1);
        ['x', 'y', 'z'].forEach(function (name, currentAxis) {
            var source = currentAxis === axis ? hostBounds : bounds;
            var snapped = currentAxis === axis
                ? snapOutward(source[name + 'min'], source[name + 'max'], size)
                : snapOpening(source[name + 'min'], source[name + 'max'], size, openingVoxelPolicy);
            bounds[name + 'min'] = snapped.min;
            bounds[name + 'max'] = snapped.max;
        });
        return inflateThinBounds(bounds, 0.02);
    }

    function normalizeOpeningVoxelPolicy(policy) {
        return policy === 'larger' ? 'larger' : 'smaller';
    }

    function snapOpening(min, max, size, openingVoxelPolicy) {
        if (normalizeOpeningVoxelPolicy(openingVoxelPolicy) !== 'smaller') {
            return snapOutward(min, max, size);
        }

        var snapped = {
            min: Math.ceil(min / size - 0.5) * size,
            max: (Math.floor(max / size - 0.5) + 1) * size
        };
        if (snapped.max <= snapped.min) return snapAroundCenter(min, max, size);
        return snapped;
    }

    function snapOutward(min, max, size) {
        return {
            min: Math.floor(min / size) * size,
            max: Math.ceil(max / size) * size
        };
    }

    function snapAroundCenter(min, max, size) {
        var snappedMin = Math.floor(((min + max) / 2) / size) * size;
        return { min: snappedMin, max: snappedMin + size };
    }

    function copyBounds(bounds) {
        return {
            xmin: bounds.xmin,
            xmax: bounds.xmax,
            ymin: bounds.ymin,
            ymax: bounds.ymax,
            zmin: bounds.zmin,
            zmax: bounds.zmax
        };
    }

    function inflateThinBounds(bounds, minThickness) {
        var b = copyBounds(bounds);
        inflateAxis(b, 'x', minThickness);
        inflateAxis(b, 'y', minThickness);
        inflateAxis(b, 'z', minThickness);
        return b;
    }

    function inflateAxis(bounds, axis, minThickness) {
        var minKey = axis + 'min';
        var maxKey = axis + 'max';
        var span = bounds[maxKey] - bounds[minKey];
        if (span >= minThickness) return;
        var center = (bounds[minKey] + bounds[maxKey]) / 2;
        bounds[minKey] = center - minThickness / 2;
        bounds[maxKey] = center + minThickness / 2;
    }

    function formatXb(bounds) {
        return [
            bounds.xmin, bounds.xmax,
            bounds.ymin, bounds.ymax,
            bounds.zmin, bounds.zmax
        ].map(formatNumber).join(',');
    }

    function formatNumber(value) {
        var fixed = Number(value).toFixed(6);
        return fixed.replace(/\.?0+$/, '');
    }

    function cleanId(name, index) {
        var id = String(name || 'IFC element ' + index).trim();
        if (id.length > 48) id = id.slice(0, 45) + '...';
        return id || 'IFC element ' + index;
    }

    // Two walls / slabs with the same IFC name (e.g. "Wand-001" copied across floors) used
    // to collide into a single FDS ID, which made the delete-by-id path remove *all*
    // instances when the user removed one. The exporter creates a fresh allocator at the
    // top of every export and threads it through every cleanId-producing site. The first
    // occurrence of a name keeps the name as-is (so tests that pin exact names still pass);
    // subsequent occurrences get a ":2", ":3", ... suffix.
    function makeIdAllocator() {
        var counts = {};
        return function (name, index) {
            var base = cleanId(name, index);
            if (counts[base] === undefined) {
                counts[base] = 1;
                return base;
            }
            counts[base] += 1;
            var suffix = ':' + counts[base];
            var maxBase = Math.max(1, 48 - suffix.length);
            var truncated = base.length > maxBase ? base.slice(0, Math.max(1, maxBase - 3)) + '...' : base;
            return truncated + suffix;
        };
    }

    function escapeFdsString(value) {
        return String(value).replace(/'/g, "''");
    }

    function surfaceForElement(element) {
        if (element.ifcType === 'IFCDOOR') return 'IFC_DOOR';
        if (element.ifcType === 'IFCWINDOW') return 'IFC_WINDOW';
        return 'IFC_SOLID';
    }

    function formatIfcSourceComment(element, outputType, itemId) {
        return [
            '! IFC_SOURCE',
            "OUTPUT='" + escapeFdsString(outputType) + "'",
            "ITEM_ID='" + escapeFdsString(itemId) + "'",
            "IFC_TYPE='" + escapeFdsString(element.ifcType || '') + "'",
            "GLOBAL_ID='" + escapeFdsString(element.globalId || '') + "'",
            "STEP_ID='" + escapeFdsString('#' + (element.stepId || '')) + "'",
            "NAME='" + escapeFdsString(element.name || '') + "'"
        ].join(', ');
    }

    function spaces(count) {
        return new Array(count + 1).join(' ');
    }

    ns.fds.FdsExporter = FdsExporter;
})(window.IfcFds);
