const fs = require('fs');
const path = require('path');

const root = path.resolve(__dirname, '..');
const dependencyFiles = [
    'js/core/namespace.js',
    'js/core/diagnostics.js',
    'js/fds/fds-exporter.js',
    'js/fds/namelist-parser.js',
    'js/fds/fds-geometry-adapter.js'
];
const workerPath = path.join(root, 'js/workers/fds-export-worker.js');
const outputPath = path.join(root, 'js/workers/fds-export-worker-inline.js');
const workerText = fs.readFileSync(workerPath, 'utf8');
const workerBodyIndex = workerText.indexOf('var exporter =');

if (workerBodyIndex === -1) {
    throw new Error('Could not find the export worker body.');
}

const workerSource = [
    'self.window = self;',
    ...dependencyFiles.map((file) => fs.readFileSync(path.join(root, file), 'utf8')),
    workerText.slice(workerBodyIndex)
].join('\n\n');

const output = [
    '// Generated by scripts/generate-inline-export-worker.js. Do not edit directly.',
    `window.IfcFdsExportWorkerSource = ${JSON.stringify(workerSource)};`,
    ''
].join('\n');

fs.writeFileSync(outputPath, output);
console.log(`Generated ${path.relative(root, outputPath)} (${output.length} characters).`);
