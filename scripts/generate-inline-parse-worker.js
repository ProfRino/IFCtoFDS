const fs = require('fs');
const path = require('path');

const root = path.resolve(__dirname, '..');
const dependencyFiles = [
    'js/vendor/earcut.min.js',
    'js/core/namespace.js',
    'js/core/diagnostics.js',
    'js/ifc/ifc-adapter.js'
];
const workerPath = path.join(root, 'js/workers/ifc-parse-shard-worker.js');
const outputPath = path.join(root, 'js/workers/ifc-parse-shard-worker-inline.js');
const workerText = fs.readFileSync(workerPath, 'utf8');
const workerBodyIndex = workerText.indexOf('var IfcAdapter =');

if (workerBodyIndex === -1) {
    throw new Error('Could not find the parse-shard worker body.');
}

const workerSource = [
    'self.window = self;',
    ...dependencyFiles.map((file) => fs.readFileSync(path.join(root, file), 'utf8')),
    workerText.slice(workerBodyIndex)
].join('\n\n');

const output = [
    '// Generated by scripts/generate-inline-parse-worker.js. Do not edit directly.',
    `window.IfcFdsParseShardWorkerSource = ${JSON.stringify(workerSource)};`,
    ''
].join('\n');

fs.writeFileSync(outputPath, output);
console.log(`Generated ${path.relative(root, outputPath)} (${output.length} characters).`);
